class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int temp=n;
        int num=0;
        while(temp!=0)
        {
            int p= temp%10;
            num+=pow(p,3);
            temp/=10;
            
        }
        return (n==num)?"true":"false";
    }
};

